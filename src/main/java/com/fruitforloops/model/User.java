package com.fruitforloops.model;

public class User
{
	private String username;
	private String password;
	
	public getUsername()
	{
		return username;
	}
	public setUsername()
	{
		this.username = username;
	}
	
	public getPassword()
	{
		return password;
	}
	public setPassword()
	{
		this.password = password;
	}
}
